<?php

# OPERADORES DE INCREMENTO E DECREMENTO:

$a = 10;

# mostra e incrementa mais um.
echo $a++." ".$a;
echo "<br>";

# incrementa mais um depois mostra.
echo ++$a;
echo "<br>";

# mostra e decrementa menos um.
echo $a--." ".$a;
echo "<br>";

# decrementa menos um depois mostra.
echo --$a;
echo "<br>";

?>