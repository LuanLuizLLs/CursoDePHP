<?php

# OPERADORES DE ATRIBUIÇÃO:

# = atribuição.
$nome = "Luan";
# . concatenação.
$nome = "Luan"." "."Luiz";
# .= concatenção com atribuição.
$nome .= " da Silva";

echo $nome;
echo "<br>";

# += mais igual.
$valorTotal = 50;
$valorTotal += 50;

echo $valorTotal;
echo "<br>";

# -= menos igual.
$valorTotal = 75;
$valorTotal -= 25;

echo $valorTotal;
echo "<br>";

# -= vezes igual.
$valorTotal = 5;
$valorTotal *= 5;

echo $valorTotal;
echo "<br>";

?>